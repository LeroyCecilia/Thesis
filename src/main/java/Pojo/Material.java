package Pojo;

public class Material {
    /**Description：原材料实体，用于描述原材料信息*/
    //private attributes
    private String materialCode;
    private String materialName;
    private String qualityLevel;
    private float elasticity;
    private float airTightness;
    private float tearStrength;
    private float abradability;
    private float heatResistance;
    private float waterResistance;
    private float acidResistance;
    private float alkaliResistance;
    private float density;
    private float insulationProperty;
    private float volumeResistance;

    //setters and getters

    public String getMaterialCode() {
        return materialCode;
    }

    public void setMaterialCode(String materialCode) {
        this.materialCode = materialCode;
    }

    public String getMaterialName() {
        return materialName;
    }

    public void setMaterialName(String materialName) {
        this.materialName = materialName;
    }

    public String getQualityLevel() {
        return qualityLevel;
    }

    public void setQualityLevel(String qualityLevel) {
        this.qualityLevel = qualityLevel;
    }

    public float getElasticity() {
        return elasticity;
    }

    public void setElasticity(float elasticity) {
        this.elasticity = elasticity;
    }

    public float getAirTightness() {
        return airTightness;
    }

    public void setAirTightness(float airTightness) {
        this.airTightness = airTightness;
    }

    public float getTearStrength() {
        return tearStrength;
    }

    public void setTearStrength(float tearStrength) {
        this.tearStrength = tearStrength;
    }

    public float getAbradability() {
        return abradability;
    }

    public void setAbradability(float abradability) {
        this.abradability = abradability;
    }

    public float getHeatResistance() {
        return heatResistance;
    }

    public void setHeatResistance(float heatResistance) {
        this.heatResistance = heatResistance;
    }

    public float getWaterResistance() {
        return waterResistance;
    }

    public void setWaterResistance(float waterResistance) {
        this.waterResistance = waterResistance;
    }

    public float getAcidResistance() {
        return acidResistance;
    }

    public void setAcidResistance(float acidResistance) {
        this.acidResistance = acidResistance;
    }

    public float getAlkaliResistance() {
        return alkaliResistance;
    }

    public void setAlkaliResistance(float alkaliResistance) {
        this.alkaliResistance = alkaliResistance;
    }

    public float getDensity() {
        return density;
    }

    public void setDensity(float density) {
        this.density = density;
    }

    public float getInsulationProperty() {
        return insulationProperty;
    }

    public void setInsulationProperty(float insulationProperty) {
        this.insulationProperty = insulationProperty;
    }

    public float getVolumeResistance() {
        return volumeResistance;
    }

    public void setVolumeResistance(float volumeResistance) {
        this.volumeResistance = volumeResistance;
    }
}

